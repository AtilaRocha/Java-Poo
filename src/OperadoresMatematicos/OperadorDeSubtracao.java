package src.OperadoresMatematicos;

public class OperadorDeSubtracao {
    public static void main(String[] args) {
        int subtracao = 10 - 4;
        System.out.println("O valor da subtração 10 - 4 = " + subtracao);
        int num1 = 10, num2 = 4;
        int subtracao2 = num1 - num2;
        System.out.println("O valor da subtração entre " + num1 + " - " + num2 + " = " + subtracao2);
    }
}
