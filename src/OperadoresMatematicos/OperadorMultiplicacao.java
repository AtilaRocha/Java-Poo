package src.OperadoresMatematicos;

public class OperadorMultiplicacao {
    public static void main(String[] args) {
        int multiplicacao = 10*5;
        System.out.println("O valor da Multiplicação 10 * 5 = "+multiplicacao);
        int num1 = 10, num2 = 5;
        int multiplicacao2 = num1*num2;
        System.out.println("O valor da Multiplicação "+num1+" * "+num2+" = "+multiplicacao2);
    }
}
