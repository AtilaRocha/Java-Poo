package src.OperadoresMatematicos;

public class OperadorDeAdicao {
    public static void main(String[] args) {
        int soma = 5 + 2;
        System.out.println("O valor da soma 5 + 2 = "+ soma);
        int num1 = 5;
        int num2 = 2;
        int soma2 = num1 + num2;

        System.out.println("O valor da soma "+num1+" + "+num2+" = "+soma2);
    }
}
