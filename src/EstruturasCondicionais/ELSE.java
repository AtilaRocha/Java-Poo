package src.EstruturasCondicionais;

public class ELSE {
    public static void main(String[] args) {
        int idade1 = 100, idade2 = 50;
        if (idade1 > idade2) {
            System.out.println("A idade1 é maior que a idade2");
        }else {
            System.out.println("A idade2 é maior que a idade1");
        }
    }
}
